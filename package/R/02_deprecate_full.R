deprecate_full <- function(.func, .msg = NULL) {
    func_name <- as.character(rlang::ensym(.func))

    warn_msg <- glue::glue("Object `{func_name}` is no longer available.\r\n")
    if (!rlang::is_null(.msg))
        warn_msg <- glue::glue("{warn_msg}{.msg}\r\n")

    func <- function(...) rlang::abort(warn_msg, "unavailable")

    return(func)
}